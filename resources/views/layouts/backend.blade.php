    <script>
       $(document).ready(function(){      
      var postURL = "<?php echo url('/add-options'); ?>";
      var i=2;  


      $('#add_options').click(function(){  
           i++;  
                                         
           $('#dynamic_field').append('<tr id="row'+i+'" class="dynamic-added"><td><input type="text" name="option_new[]" value="" class="form-control"></td><td style="text-align:center;"><select name="status_new[]" class="form-control select-status"><option value="1" selected > Active</option><option value="0" >In Active</option></select> </td><td style="text-align:center;"><input type="text" name="position_new[]"  class="form-control"></td><td><button type="button" name="remove" id="'+i+'" class="btn btn-danger btn_remove">X</button></td></tr>');  
      });  


      $(document).on('click', '.btn_remove', function(){  
           var button_id = $(this).attr("id");   
           $('#row'+button_id+'').remove();  
      });  


      $.ajaxSetup({
          headers: {
            'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
          }
      });


      $('#submit_options').click(function(){   
          var survey_id=$('#survey_id').val(); 
          var question_id=$('#question_id').val();
          var options=$("input[name='option_new[]']").map(function(){return $(this).val();}).get();
          //var status=$("input[name='status_new[]']").map(function(){return $(this).val();}).get();
         var  statusList = [];
            $('.select-status').each(function() {
              statusList.push($(this).val())
            });
          
          var positions=$("input[name='position_new[]']").map(function(){return $(this).val();}).get(); 
          // alert(survey_id);
          // alert(question_id);
          $.ajax({  
                url:postURL,  
                method:"POST",  
                data:{"survey_id":survey_id,"question_id":question_id,"options":options,"status":statusList,"positions":positions,"_token": "{{ csrf_token() }}"},
                type:'json',
                success:function(data)  
                {
                    if(data.error){
                        printErrorMsg(data.error);
                    }else{
                        i=1;
                        toastr.success(data.message);
                        location.reload();
                       
                       
                      }
                }  
           });  
      });  


      function printErrorMsg (msg) {
         $(".print-error-msg").find("ul").html('');
         $(".print-error-msg").css('display','block');
         $(".print-success-msg").css('display','none');
         $.each( msg, function( key, value ) {
            $(".print-error-msg").find("ul").append('<li>'+value+'</li>');
         });
      }
    }); 

    </script>
